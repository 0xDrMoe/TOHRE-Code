</li>
<li>
<p><code v-pre>template.txt</code>: You can modify the <code v-pre>welcome</code> and <code v-pre>onMeeting</code> messages here. You can also add custom templates here as well following the same format as <code v-pre>welcome</code> &amp; <code v-pre>onMeeting</code> use.</p>
</li>
<li>
<p><code v-pre>VIP-List.txt</code>: A list of friendcodes that will be given a sweet VIP tag (editable via <code v-pre>...\Among Us\Language\english.dat</code>) as well as permissions to change their name color.</p>
</li>
<li>
<p><code v-pre>WhiteList.txt</code>: A list of friendcodes that will be exempt from blacklisted platforms, along with level requirements.</p>
</li>
