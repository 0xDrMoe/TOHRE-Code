<summary><b><font color=gray>Text Formatting</font></b></summary>
<ul>
<li><code v-pre>&lt;color=#[hex]&gt;exampleText&lt;/color&gt;</code> Changes the <span style="background-image: linear-gradient(to right, red, orange, yellow, green, blue, indigo, violet); -webkit-background-clip: text; color: transparent;"> Color </span> of the text/font.</li>
