<li><code v-pre>{{VotingTime}}</code> - Displays the time period where you vote.</li>
<li><code v-pre>{{Date}}</code> - Displays the current date.</li>
<li><code v-pre>{{Time}}</code> - Displays the current time.</li>
<li><code v-pre>{{RoomCode}}</code> - Displays the Room Code.</li>
<li><code v-pre>{{PlayerName}}</code> - Displays the players username. (The one reading the variable)</li>
<li><code v-pre>{{HostName}}</code> - Displays the hosts username</li>
<li><code v-pre>{{ModVersion}}</code> - Displays the current version of TOHE.</li>
<li><code v-pre>{{Map}}</code> - Displays the Map that is going to be played.</li>
